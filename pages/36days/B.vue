	const toggleTimeline = () => {
		tl.value.reversed(!tl.value.reversed())
	}

	const playTimeline = () => {
		tl.value.play()
	}

	const pauseTimeline = () => {
		tl.value.pause()
	}

	const restartTimeline = () => {
		tl.value.restart()
	}

	const reverseTimeline = () => {
		tl.value.reverse()
	}

	onMounted(() => {
		ctx.value = gsap.context((self) => {
			const dot = self.selector(".dot")
			tl.value = gsap
				.timeline()
